def _fill_front(lst: list, to: int):
	rem = to - len(lst)
	assert rem >= 0
	return [None] * rem + lst


def load_dbf(dbf):
	mercenaries_by_id = []

	for mercenary in dbf:
		if not mercenary["collectible"]:
			continue

		mercenary_id = int(mercenary["id"])
		skin_card_ids = mercenary["art_variation_ids"]
		assert len(skin_card_ids) == 3

		abilities = []
		for ability in mercenary["abilities"]:
			level_card_ids = ability["tier_ids"]
			assert len(level_card_ids) == 5
			abilities.append(
				{"ability_id": ability["id"], "tiers": level_card_ids}
			)

		equipment = []
		for equipment_piece in mercenary["equipment"]:
			level_card_ids = equipment_piece["tier_ids"]
			assert len(level_card_ids) <= 4
			equipment.append(
				{
					"equipment_id": equipment_piece["id"],
					"tiers": _fill_front(level_card_ids, 4),
				}
			)

		merc = {
			"mercenary_id": mercenary_id,
			"skins": skin_card_ids,
			"abilities": abilities,
			"equipment": equipment,
		}

		mercenaries_by_id.append(merc)

	return mercenaries_by_id


